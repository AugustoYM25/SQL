CREATE OR REPLACE 
FUNCTION RETORNA_SALARIO  (P_ID IN EMPLOYEES.EMPLOYEE_ID%TYPE)
 
RETURN NUMBER
 
IS    
      V_SALARY EMPLOYEES.SALARY%TYPE;
      V_QTDE NUMBER(1);
    BEGIN   
        SELECT COUNT(*) INTO V_QTDE
          FROM EMPLOYEES WHERE EMPLOYEE_ID = P_ID; 
        IF V_QTDE = 1 THEN
          SELECT SALARY INTO V_SALARY
            FROM EMPLOYEES WHERE EMPLOYEE_ID = P_ID;
         RETURN V_SALARY;
        ELSE
          RETURN -1;
        END IF;
END;
 
 
 
DECLARE 
  RETORNO NUMBER;
BEGIN
  RETORNO :=   RETORNA_SALARIO(105);  
  DBMS_OUTPUT.PUT_LINE ('Salario: ' || RETORNO);
END;
