-- Tabela e sequence de USUARIO------------------------------------------------
CREATE TABLE USUARIO(
    ID_USU NUMBER(7) PRIMARY KEY,
    NOME VARCHAR2(120) NOT NULL,
    EMAIL VARCHAR2(120) NOT NULL,
    SENHA VARCHAR2(120) NOT NULL
);
 
CREATE SEQUENCE SEQ_USU_ID
START WITH 1
MAXVALUE 9999999
NOCACHE
NOCYCLE;


-- Tabela e sequence de Login ------------------------------------------------
CREATE TABLE LOG_LOGIN(
 
LOG_ID NUMBER(10,0),
NOME_CODIGO	VARCHAR2(30 ),
PARAMETROS_ENTRADA	VARCHAR2(1000 ),
CODIGO_ERRO	NUMBER(5,0),
DESCRICAO_ERRO	VARCHAR2(240 ),
LINHA_ERRO	VARCHAR2(240 ),
DTH_REGISTRO	DATE,
USUARIO	VARCHAR2(30 ),
STATUS	CHAR(1 ),
DESCRICAO_RESOLVIDO	VARCHAR2(1000 ));

CREATE SEQUENCE SQ_ID_LOG_LOGIN
START WITH 1
MAXVALUE 9999999
NOCACHE
NOCYCLE;


-- Tabela e sequence de cadastro ------------------------------------------------
CREATE SQ_ID_LOG_CADASTRO
START WITH 1
MAXVALUE 9999999
NOCACHE
NOCYCLE;


CREATE TABLE LOG_CADASTRO(
 
LOG_ID NUMBER(10,0),
NOME_CODIGO	VARCHAR2(30 ),
PARAMETROS_ENTRADA	VARCHAR2(1000 ),
CODIGO_ERRO	NUMBER(5,0),
DESCRICAO_ERRO	VARCHAR2(240 ),
LINHA_ERRO	VARCHAR2(240 ),
DTH_REGISTRO	DATE,
USUARIO	VARCHAR2(30 ),
STATUS	CHAR(1 ),
DESCRICAO_RESOLVIDO	VARCHAR2(1000 ));
